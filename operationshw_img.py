import cv2
import numpy as np
img = cv2.imread("/Users/muhammadtaqiriaz/Documents/opencv/photo-1481349518771-20055b2a7b24 (1).jpeg",1)
cv2.imshow("Image 1",img)
img_small = cv2.resize(img,(500,350))
cv2.imshow("Image 1 resize",img_small)
img_2 = cv2.imread("/Users/muhammadtaqiriaz/Documents/opencv/photo-1494253109108-2e30c049369b.jpeg",1)
cv2.imshow("Image 2",img_2)
img2_small = cv2.resize(img_2,(500,350))
cv2.imshow("Image 2 resize",img2_small)
img_weighted = cv2.addWeighted(img_small,0.5,img2_small,0.5,-25)
cv2.imshow("Image weighted",img_weighted)
img_subtract = cv2.subtract(img_small,img2_small)
cv2.imshow("Image subrtact",img_subtract)
kernel = np.ones((5,5),np.uint8)
img_erosion = cv2.erode(img_small,kernel)
cv2.imshow("Image 1 erosion",img_erosion)
img_blur = cv2.GaussianBlur(img_small,(15,15),0)
cv2.imshow("Image 1 blur",img_blur)
